<script type="text/javascript">
	try
	{
	$("#search_ip").on('keyup',function(){

		$(".s_i").html("<i class='fa fa-spinner fa-spin'></i>");

		var key = this.value.toLowerCase();
	
		var box_list = $(".content").find('.box');
		
		$(box_list).each(function(k,v){

			var sub = $(v).find('.col-lg-3');
			var chk = 0;
			$(sub).each(function(a,b){
					 var text = $(b).first('a').text().toLowerCase();

					if(text.includes(key)){
						$(b).show();
						chk = 1;
					}
					else
						$(b).hide();
			});

			if(chk)
				$(v).show();
			else
				$(v).hide();

		});
		$(".s_i").html("<i class='fa fa-search'></i>");
	});
}catch(e){alert(e);}
</script>